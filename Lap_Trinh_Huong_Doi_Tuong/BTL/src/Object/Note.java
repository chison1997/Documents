package Object;


public class Note {
    int MAN;
    private String noidung;

    public Note() {
    }

    public Note(int MAN, String noidung) {
        this.noidung = noidung;
    }

    public int getMAN() {
        return MAN;
    }

    public void setMAN(int MAN) {
        this.MAN = MAN;
    }

    public String getNoidung() {
        return noidung;
    }

    public void setNoidung(String noidung) {
        this.noidung = noidung;
    }

   
    
  
    
}
